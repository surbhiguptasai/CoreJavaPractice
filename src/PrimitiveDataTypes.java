public class PrimitiveDataTypes {
    public static void main(String[] args) {
        int index = 10;
        float area = 25.0f;
        boolean isMarried = true;
        double amount = 2500.5;
        System.out.println("Int value:"+index);
        System.out.println("float value:"+area);
        System.out.println("boolean value:"+isMarried);
        System.out.println("double value:"+amount);
    }
}